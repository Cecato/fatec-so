var valorA;
var valorB;

valorA = parseFloat(prompt("Digite o valor de a"));
valorB = parseFloat(prompt("Digite o valor de b"));

var soma = valorA + valorB;
var sub = valorA - valorB;
var prod = valorA * valorB;
var div = valorA / valorB;
var resto = valorA % valorB;

alert('Soma: ' + soma);
alert('Subtração: ' + sub);
alert('Produto: ' + prod);
alert('Divisão: ' + div);
alert('Resto: ' + resto);